enum Geometry {
    private static let earthRadiusMeters = 6_378_137.0

    static func headingToVector(_ headingDegrees: Double) -> Vector2D {
        let radians = headingDegrees * .pi / 180.0
        return Vector2D(x: sin(radians), y: cos(radians))
    }

    static func coordinateToLocalPoint(origin: GeoCoordinate, coordinate: GeoCoordinate) -> Vector2D {
        let lat0 = origin.latitude * .pi / 180.0
        let dLat = (coordinate.latitude - origin.latitude) * .pi / 180.0
        let dLon = (coordinate.longitude - origin.longitude) * .pi / 180.0

        return Vector2D(
            x: earthRadiusMeters * dLon * cos(lat0),
            y: earthRadiusMeters * dLat
        )
    }

    static func localPointToCoordinate(origin: GeoCoordinate, point: Vector2D) -> GeoCoordinate {
        let lat0 = origin.latitude * .pi / 180.0
        let dLat = point.y / earthRadiusMeters
        let dLon = point.x / (earthRadiusMeters * max(0.01, cos(lat0)))

        return GeoCoordinate(
            latitude: origin.latitude + (dLat * 180.0 / .pi),
            longitude: origin.longitude + (dLon * 180.0 / .pi)
        )
    }

    static func segmentsIntersect(_ p1: Vector2D, _ p2: Vector2D, _ q1: Vector2D, _ q2: Vector2D) -> Bool {
        let r = p2 - p1
        let s = q2 - q1
        let denominator = cross(r, s)
        let qMinusP = q1 - p1

        if abs(denominator) < 1e-9 {
            return false
        }

        let t = cross(qMinusP, s) / denominator
        let u = cross(qMinusP, r) / denominator

        return (0.0 ... 1.0).contains(t) && (0.0 ... 1.0).contains(u)
    }

    static func cross(_ a: Vector2D, _ b: Vector2D) -> Double {
        (a.x * b.y) - (a.y * b.x)
    }
}
