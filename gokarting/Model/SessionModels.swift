import Foundation
import CoreLocation

struct GeoCoordinate: Hashable, Codable {
    var latitude: Double
    var longitude: Double

    init(latitude: Double, longitude: Double) {
        self.latitude = latitude
        self.longitude = longitude
    }

    init(_ coordinate: CLLocationCoordinate2D) {
        self.latitude = coordinate.latitude
        self.longitude = coordinate.longitude
    }

    var clCoordinate: CLLocationCoordinate2D {
        CLLocationCoordinate2D(latitude: latitude, longitude: longitude)
    }
}

struct Vector2D: Hashable, Codable {
    var x: Double
    var y: Double

    var magnitude: Double { sqrt((x * x) + (y * y)) }

    func normalized() -> Vector2D {
        let mag = magnitude
        guard mag > 1e-9 else { return Vector2D(x: 0, y: 0) }
        return Vector2D(x: x / mag, y: y / mag)
    }

    static func +(lhs: Vector2D, rhs: Vector2D) -> Vector2D {
        Vector2D(x: lhs.x + rhs.x, y: lhs.y + rhs.y)
    }

    static func -(lhs: Vector2D, rhs: Vector2D) -> Vector2D {
        Vector2D(x: lhs.x - rhs.x, y: lhs.y - rhs.y)
    }

    static func *(lhs: Vector2D, rhs: Double) -> Vector2D {
        Vector2D(x: lhs.x * rhs, y: lhs.y * rhs)
    }

    func dot(_ other: Vector2D) -> Double {
        (x * other.x) + (y * other.y)
    }
}

enum RaceDirection: String, CaseIterable, Identifiable, Codable {
    case clockwise = "Clockwise"
    case counterClockwise = "Counterclockwise"

    var id: String { rawValue }
}

struct StartFinishGate: Hashable, Codable {
    var pointA: GeoCoordinate
    var pointB: GeoCoordinate
    var expectedForward: Vector2D

    var center: GeoCoordinate {
        GeoCoordinate(
            latitude: (pointA.latitude + pointB.latitude) / 2.0,
            longitude: (pointA.longitude + pointB.longitude) / 2.0
        )
    }

    static func autoGenerated(center: GeoCoordinate, headingDegrees: Double, widthMeters: Double) -> StartFinishGate {
        let forward = Geometry.headingToVector(headingDegrees).normalized()
        let perpendicular = Vector2D(x: -forward.y, y: forward.x).normalized()
        let halfWidth = max(1.0, widthMeters) / 2.0

        let a = Geometry.localPointToCoordinate(
            origin: center,
            point: perpendicular * halfWidth
        )
        let b = Geometry.localPointToCoordinate(
            origin: center,
            point: perpendicular * -halfWidth
        )

        return StartFinishGate(pointA: a, pointB: b, expectedForward: forward)
    }
}

